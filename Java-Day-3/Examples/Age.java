class Age
{
	private int day;
	private int month;
	private int year;

	public int getDay()
	{
		return day;
	}

	public int setDay(int day)
	{
		this.day = day;
	}

	public int getMonth()
	{
		return month;
	}

	public int setMonth(int day)
	{
		this.month = month;
	}

	public int getYear()
	{
		return year;
	}

	public int setYear(int day)
	{
		this.year = year;
	}
}